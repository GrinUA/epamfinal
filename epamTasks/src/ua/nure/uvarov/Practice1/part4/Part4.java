package ua.nure.uvarov.Practice1.part4;

public class Part4 {
    public static void main(String[] args) {
        int number = Integer.parseInt(args[0]);
        int result = 0;
        while (number != 0) {
            result += number % 10;
            number /= 10;
        }
        System.out.println(result);
    }
}
